grade=int(input("Enter the grade"))

if (grade>=90 and grade<=100):
    print("A")
if (grade >=80 and grade <=90):
    print("B")
if (grade>=70 and grade <=80):
    print("C")
if (grade>=60 and grade <=70):
    print("D")
else:
    print("you fail")

